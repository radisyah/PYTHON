# For dengan list
angka_list =[1,2,3,4,5]
print("\n",angka_list)
for i in angka_list:
  print(f"angka --> {i}")

#For dengan Range
angka_range=range(1,6)
print("\n",angka_range)
for i in angka_range:
  print(f"angka --> {i}")

#For dengan String
string = "DIMAS"
print("\n",string)
for i in string:
  print(f"string --> {i}")


